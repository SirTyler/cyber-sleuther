class Digimon:
    def __init__(self, _id, _name, _hp, _sp, _atk, _def, _int, _spd, _abi, _cam, _skills, _known, _slots, _equip):
        self.ID = _id
        self.NAME = _name
        self.HP = _hp
        self.SP = _sp
        self.ATK = _atk
        self.DEF = _def
        self.INT = _int
        self.SPD = _spd
        self.ABI = _abi
        self.CAM = _cam
        self.skills = []
        for s in _skills:
            self.skills.append(Skill(s))
        self.known_skills = []
        for s in _known:
            self.known_skills.append(Skill(s))
        self.equipment_slots = _slots
        self.equipment = []
        for e in _equip:
            self.equipment.append(Item(e))
    def __str__(self):
        return str.format("#"+str(self.ID)+" "+self.NAME)
